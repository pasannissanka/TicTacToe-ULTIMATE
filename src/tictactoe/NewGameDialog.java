    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Player player1, player2;
        if (this.jComboBox1.getSelectedItem() == "AI") {
            player1 = new AIPlayer(PlayerState.CROSS, "Cross");
            player2 = new NoughtPlayer(this.jTextField2.getText());
        } else if (this.jComboBox2.getSelectedItem() == "AI"){
            player1 = new CrossPlayer(this.jTextField1.getText());
            player2 = new AIPlayer(PlayerState.NOUGHT, "Nought");
        } else {
            player1 = new CrossPlayer(this.jTextField1.getText());
            player2 = new NoughtPlayer(this.jTextField2.getText());
        }

        Launcher.gameState = new GameState(player1, player2);
        GameUI game = new GameUI(Launcher.gameState);
        deleteSerializationFiles();
        game.start();
        this.setVisible(false);
        Launcher.mainScreen.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

