    private void deleteSerializationFiles() {
        File fileGS = new File("./tmp/gameState.ser");
        File fileP1 = new File("./tmp/player1.ser");
        File fileP2 = new File("./tmp/player2.ser");

        try {
            fileGS.delete();
            fileP1.delete();
            fileP2.delete();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

